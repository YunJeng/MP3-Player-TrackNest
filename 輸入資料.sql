from (((song inner join Artist on song.artistID = Artist.ArtistID)
		inner join Album on song.AlbumID = Album.AlbumID)
        inner join Category on song.CategoryID = Category.CategoryID)
        inner join Composer on song.ComposerID = Composer.ComposerID where songID = 1;

